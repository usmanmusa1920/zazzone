function show_all_result(){
    document.querySelector('.all_result').style.display = 'block';
    document.querySelector('.people_result').style.display = 'none';
    document.querySelector('.user_board_result').style.display = 'none';
    document.querySelector('.blog_result').style.display = 'none';
    document.querySelector('.board_result').style.display = 'none';
    document.querySelector('.zone_result').style.display = 'none';

    // These will change the backgroundcolor and color of clicked button
    document.querySelector('.all_result_btn').style.backgroundColor = 'grey';
    document.querySelector('.all_result_btn').style.color = 'white';
    // For user account
    document.querySelector('.people_result_btn').style.backgroundColor = 'white';
    document.querySelector('.people_result_btn').style.color = 'grey';
    // For user board
    document.querySelector('.board_result_btn').style.backgroundColor = 'white';
    document.querySelector('.board_result_btn').style.color = 'grey';
    // For blog post
    document.querySelector('.blog_post_result_btn').style.backgroundColor = 'white';
    document.querySelector('.blog_post_result_btn').style.color = 'grey';
    // For board post
    document.querySelector('.board_post_result_btn').style.backgroundColor = 'white';
    document.querySelector('.board_post_result_btn').style.color = 'grey';
    // For zone
    document.querySelector('.zone_result_btn').style.backgroundColor = 'white';
    document.querySelector('.zone_result_btn').style.color = 'grey';
}


